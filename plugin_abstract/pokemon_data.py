class PokemonDataManager(ABC):
    """
    A class that manages game data for a PorySuite project.

    This class provides methods to add, retrieve, and manipulate various types of game data, such as species data,
    graphics, abilities, items, constants, starters, moves, and Pokédex information.

    The `PokemonDataManager` class serves as a central hub for accessing and modifying different types of Pokemon data
    within a project.
    """
    SOURCE_PREFIX = AbstractPokemonData.SOURCE_PREFIX

    def __init__(self, project_info: dict, logger=None):
        self.logger = logger

        def init_body():
            self.project_info = dict(project_info)
            if "source_prefix" not in self.project_info:
                self.project_info["source_prefix"] = self.SOURCE_PREFIX
            util = LocalUtil(self.project_info)
            resolved = util.repo_root()
            if resolved != self.project_info.get("dir"):
                if not (
                    os.path.isdir(os.path.join(resolved, "src"))
                    and os.path.isdir(os.path.join(resolved, "include"))
                ):
                    raise RuntimeError(
                        "Resolved repo_root does not contain required folders: "
                        f"{resolved}"
                    )
                print(
                    f"repo_root mismatch: selected {self.project_info.get('dir')} resolved {resolved}"
                )
            else:
                print(f"repo_root confirmed: {resolved}")
            self.data = {}

        if self.logger:
            buf = io.StringIO()
            with redirect_stdout(buf):
                init_body()
            output = buf.getvalue().strip()
            if output:
                for line in output.splitlines():
                    self.logger(line)
        else:
            init_body()
        
    def __add_data_class(self, class_obj: Type, key: str):
        instance = class_obj(self.project_info, self)
        if instance.data is None:
            instance.data = instance.default_data() if hasattr(instance, "default_data") else {}
