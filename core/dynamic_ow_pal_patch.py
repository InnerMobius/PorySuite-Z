MARKER_FILE = ".porysuite_dowp_enabled"


def is_dowp_enabled(project_root: str) -> bool:
    """Check whether the dynamic OW palette patch has actually been applied.

    Verifies by inspecting the real source code for DOWP signatures,
    not just checking for the marker file.  This handles cases where
    the pokefirered folder was replaced or restored from a clean copy.
    If the source is patched but the marker is missing, re-creates it.
    If the marker exists but the source is unpatched, removes the stale marker.
    """
    marker_path = os.path.join(project_root, MARKER_FILE)
    marker_exists = os.path.isfile(marker_path)

    # Check the actual source for DOWP signatures
    eom_path = os.path.join(project_root, "src", "event_object_movement.c")
    source_patched = False
    if os.path.isfile(eom_path):
        try:
            with open(eom_path, encoding="utf-8", errors="replace") as f:
                eom_text = f.read()
            # These are unique strings that only exist after DOWP patching
            source_patched = (
                "FieldEffectFreePaletteIfUnused(paletteNum);" in eom_text
                and "// DOWP: palettes load on demand" in eom_text
            )
        except OSError:
            # Can't read the file — fall back to marker
            return marker_exists

    # Sync the marker file with reality
    if source_patched and not marker_exists:
        # Source is patched but marker is missing — recreate it
        try:
            with open(marker_path, "w") as f:
                f.write(
                    "Dynamic Overworld Palettes (DOWP) enabled by PorySuite-Z.\n"
                    "This file marks that the DOWP patch has been applied to this project.\n"
                    "Do not delete — PorySuite-Z uses this to detect patch status.\n"
                )
        except OSError:
            pass
    elif not source_patched and marker_exists:
        # Marker is stale (project folder was replaced) — remove it
        try:
            os.remove(marker_path)
        except OSError:
            pass

    return source_patched


def _read(path: str) -> str:
    with open(path, encoding="utf-8", errors="replace") as f:
        return f.read()


def _write(path: str, content: str) -> None:
    with open(path, "w", encoding="utf-8", newline="\n") as f:
        f.write(content)


def _replace_once(text: str, old: str, new: str, desc: str) -> Tuple[str, bool]:
    """Replace exactly one occurrence of 'old' with 'new'. Returns (new_text, success)."""
    count = text.count(old)
    if count == 0:
        return text, False
    if count > 1:
        # Try to still apply — replace first occurrence
        idx = text.index(old)
        text = text[:idx] + new + text[idx + len(old):]
        return text, True
    return text.replace(old, new), True


def _replace_all(text: str, old: str, new: str) -> str:
    """Replace all occurrences."""
    return text.replace(old, new)


def scan_dowp_risks(project_root: str) -> List[str]:
    """Scan the project for known DOWP compatibility issues.
    Returns a list of plain-English warning strings (empty list = no known risks).
    """
    warnings: List[str] = []

    gfx_candidates = [
        os.path.join(project_root, "src", "data", "object_events", "object_event_graphics_info.h"),
        os.path.join(project_root, "src", "data", "graphics", "object_events", "graphics_info.h"),
    ]
    none_tag_structs: List[str] = []
    all_tags: set = set()

    for gfx_path in gfx_candidates:
        if not os.path.isfile(gfx_path):
            continue
        try:
            with open(gfx_path, encoding="utf-8", errors="replace") as f:
                text = f.read()
            for m in re.finditer(
                r'const struct ObjectEventGraphicsInfo\s+(\w+)\s*=\s*\{([^}]*)\}',
                text, re.DOTALL
            ):
                struct_name = m.group(1)
                body = m.group(2)
                tag_m = re.search(r'\.paletteTag\s*=\s*(\w+)', body)
                if tag_m:
                    tag = tag_m.group(1)
                    if tag == "OBJ_EVENT_PAL_TAG_NONE":
                        none_tag_structs.append(struct_name)
                    else:
                        all_tags.add(tag)
        except Exception:
            pass

    if none_tag_structs:
        count = len(none_tag_structs)
        names = ", ".join(none_tag_structs[:4])
        if count > 4:
            names += f" (+{count - 4} more)"
        warnings.append(
            f"{count} sprite definition(s) use OBJ_EVENT_PAL_TAG_NONE as their palette tag. "
            f"Under DOWP these sprites won't load a palette and will display the wrong colours.\n"
            f"Affected: {names}\n"
            f"Fix: assign each a unique OBJ_EVENT_PAL_TAG_* constant in object_event_graphics_info.h."
        )

    if len(all_tags) > 12:
        warnings.append(
            f"Your project uses {len(all_tags)} unique sprite palette tags. "
            f"The GBA only supports 16 OBJ palette slots at once. "
            f"The player sprite and its reflection use ~2–3 slots, and any custom HUD "
            f"overlay takes another. If more unique NPC sprites appear on screen "
            f"simultaneously than available slots, extra sprites will display wrong colours."
        )

    return warnings


