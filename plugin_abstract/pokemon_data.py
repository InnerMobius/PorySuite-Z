    def parse_to_c_code(self):
        missing = self.missing_required_sources()
        if missing:
            raise MissingSourceError(missing)
        threads = []
        for data in self.data:
            thread = ParseThread(self.data[data])
            threads.append(thread)
            thread.start()
        # Poll with a short timeout so Qt's event loop keeps running.
        # A plain thread.join() blocks the main thread indefinitely and causes
        # Windows to mark the window as "Not Responding".
        try:
            from PyQt6.QtWidgets import QApplication
            from PyQt6.QtCore import QEventLoop
            _pump = lambda: QApplication.processEvents(
                QEventLoop.ProcessEventsFlag.ExcludeUserInputEvents
            )
        except Exception:
            _pump = lambda: None
        for thread in threads:
            while thread.is_alive():
                thread.join(timeout=0.05)
                _pump()
        for thread in threads:
            if thread.exception is not None:
                raise thread.exception

