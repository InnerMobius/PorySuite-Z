    def missing_required_sources(self) -> list[str]:
        missing: list[str] = []
        for data_obj in self.data.values():
            try:
                missing.extend(data_obj.missing_required_sources())
            except Exception:
                continue
        deduped: list[str] = []
        seen: set[str] = set()
        for path in missing:
            norm = os.path.normpath(path)
            if norm not in seen:
                seen.add(norm)
                deduped.append(norm)
        return deduped

