"""Dynamic Overworld Palettes (DOWP) patch for pokefirered decomp.

Applies the community-standard dynamic palette system that allows each
overworld sprite to have its own unique palette instead of being locked
to the 4 shared NPC palette slots (Blue, Pink, Green, White).

After this patch:
  - Each sprite loads its palette on demand when it spawns
  - Palettes are freed when no sprites use them anymore
  - Reflection palettes are generated by tinting the base palette at runtime
  - Up to 16 unique overworld palettes can be on screen simultaneously
  - Users can add new sprites with fully custom palettes

This is a ONE-WAY patch. It modifies multiple C source files in the project.

Based on the community implementations by Navenatox, ExpoSeed, and others.
Adapted for pokefirered decomp by PorySuite-Z.
"""

from __future__ import annotations

