    def clear_caches(self):
        """Delete all ``src/data`` JSON files for this project.

        Uses ``project_info["dir"]`` directly (same path used by ``save()``)
        so the clear and the save always target the same directory.
        Returns the number of files removed.
        """
        root = self.project_info.get("dir", "")
        cleared = 0
        for path in glob.glob(os.path.join(root, "src", "data", "*.json")):
            try:
                os.remove(path)
                cleared += 1
            except OSError:
                pass
        return cleared

    def rebuild_caches(self, logger=None):
        """Re-extract all data from C headers and rewrite JSON cache files.

        Each extractor runs independently — a failure in one does not abort
        the others.  Progress and errors are surfaced through ``logger``.
        """
        cleared = self.clear_caches()
        if logger:
            logger(f"Cleared {cleared} cached file(s). Re-extracting from source headers...")

        failed = []
        skipped = []
        for key, data_obj in self.data.items():
            extractor = getattr(data_obj, "EXTRACTOR", None)
            if extractor is None:
                skipped.append(key)
                continue
            if logger:
                logger(f"Extracting {key}...")
            extractor.reset_cache()
            buf = io.StringIO()
            result = None
            try:
                with redirect_stdout(buf):
                    result = extractor.extract_data()
            except Exception as exc:
                failed.append((key, str(exc)))
                if logger:
                    logger(f"  ERROR extracting {key}: {exc}")
            output = buf.getvalue().strip()
            if logger and output:
                for line in output.splitlines():
                    logger(f"  {line}")
            if result:
                data_obj.data = result
                data_obj.original_data = json.loads(json.dumps(result))
                data_obj.save()
            else:
                if logger and extractor is not None and key not in [f for f, _ in failed]:
                    logger(f"  WARNING: {key} returned no data — tab may be empty")

        if skipped and logger:
            logger(f"Skipped (no extractor): {', '.join(skipped)}")
        if failed and logger:
            logger(f"Rebuild finished with {len(failed)} error(s): "
                   + ", ".join(k for k, _ in failed))
